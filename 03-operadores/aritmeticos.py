# Operadores aritmeticos

numero1 = 80
numero2 = 4 # Operador de asignación =

resta = numero1 - numero2 # Operador de resta -
multiplicacion = numero1 * numero2 # Operador de multiplicación *
division = numero1 / numero2 # Operador de división /
resto = numero1 % numero2 # Operador de módulo


print("*************** CALCULADORA ***************")
print(f"La suma es: {numero1 + numero2}")
print(f"La resta es: {resta}")
print("La multiplicación es: ", multiplicacion)
print("La división es: ", division)
print("La resto de la división es: ", resto)